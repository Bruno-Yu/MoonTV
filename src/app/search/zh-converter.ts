export class TrieNode {
  map: Map<string, TrieNode>;
  k: string;
  v: string;
  end: boolean;

  constructor() {
    this.map = new Map();
    this.k = '';
    this.v = '';
    this.end = false;
  }
}

export class Trie {
  root: TrieNode;

  constructor() {
    this.root = new TrieNode();
  }

  set(key: string, value: string): this {
    return this.add(key, value);
  }

  add(key: string, value: string): this {
    const chars = Array.from(key);
    let node = this.root;
    for (const ch of chars) {
      if (!node.map.has(ch)) {
        node.map.set(ch, new TrieNode());
      }
      node = node.map.get(ch)!;
    }
    node.end = true;
    node.k = key;
    node.v = value;
    return this;
  }

  find(text: string, start = 0): [number, string?, string?] {
    let node = this.root;
    const chars = Array.from(text);
    const matches: [number, string, string][] = [];

    for (let i = start; i < chars.length; i++) {
      const ch = chars[i];
      const next = node.map.get(ch);
      if (!next) break;
      node = next;
      if (node.end) matches.push([i, node.k, node.v]);
    }

    return matches.pop() || [start];
  }
}

// 逃脫特殊字元以安全使用正則
function escapeRegExp(str: string): string {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// 套用 Trie 轉換邏輯
function applyTrieConversion(text: string, trie: Trie): string {
  const chars = Array.from(text);
  const result: [string, string][] = [];

  for (let i = 0; i < chars.length; i++) {
    const [end, original, replacement] = trie.find(text, i);
    if (original && replacement) {
      result.push([original, replacement]);
      i = end; // skip 已處理的詞
    }
  }

  const sorted = result.sort((a, b) => b[0].length - a[0].length);
  let output = text;
  for (const [src, dst] of sorted) {
    output = output.replace(new RegExp(escapeRegExp(src), 'g'), dst);
  }

  return output;
}

